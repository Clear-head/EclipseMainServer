def _get_people_exclusion_rule(people_count: int) -> str:
    """
    인원수 관련 제외 규칙 생성

    Args:
        people_count: 인원 수

    Returns:
        인원수 관련 제외 규칙 문자열
    """
    if people_count >= 2:
        return f"- 인원 수가 {people_count}명이므로 '혼자', '1인', '솔로', '혼밥' 등 1인 관련 키워드는 제외"
    return ""


